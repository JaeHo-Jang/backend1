package a1112;

public class Escape {
    public static void main(String[] args) {
        System.out.println("'Hello'");
        System.out.println("\"Hello\"");
        //  쌍따옴표를 출력하려면   \"문자\" 형태의 특수문자 -  escape문자 형태
        //  숙제 : escape문자를 찾아 적용해 보세요.

        System.out.println("C:\\Download\\hello.java");
        //   \ 를 화면에 문자로 출력하고 싶다면 \\로 표현한다.
    }
    
}
